package com.oinsist.common.crypto.enums;

/**
 * 脱敏类型枚举
 * <p>
 * 定义了常见的敏感数据类型及对应的脱敏策略。
 * 每种类型在 SensitiveUtils 中有对应的掩码实现。
 */
public enum SensitiveType {
    /** 手机号：保留前3后4，中间用****代替。如：138****1234 */
    PHONE,
    /** 邮箱：用户名仅保留首字符，@后域名完整保留。如：t***@gmail.com */
    EMAIL,
    /** 身份证号：保留前6后4，中间用********代替。如：110101********1234 */
    ID_CARD,
    /** 银行卡号：保留前4后4，中间用****代替。如：6222****1234 */
    BANK_CARD,
    /** 姓名：仅保留姓氏，名用*代替。如：张** */
    NAME,
    /** 自定义：全部用***替换（适用于无法归类的短敏感信息） */
    CUSTOM
}
